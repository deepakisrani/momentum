// Generates supabase/migrations/0006_exercise_mechanic_and_open_seed.sql from the
// free-exercise-db (https://github.com/yuhonas/free-exercise-db, public domain / Unlicense).
// Run: node scripts/gen-exercise-seed.mjs
import { writeFile } from 'node:fs/promises'

const SOURCE = 'https://raw.githubusercontent.com/yuhonas/free-exercise-db/main/dist/exercises.json'
const OUT = new URL('../supabase/migrations/0006_exercise_mechanic_and_open_seed.sql', import.meta.url).pathname

const sql = (v) => (v == null ? 'null' : `'${String(v).replace(/'/g, "''")}'`)
const mechanic = (m) => (m === 'compound' || m === 'isolation' ? `'${m}'` : 'null')

const res = await fetch(SOURCE)
if (!res.ok) throw new Error(`fetch failed: ${res.status}`)
const all = await res.json()

// Dedupe by case-insensitive name, keep first occurrence.
const seen = new Set()
const rows = []
for (const e of all) {
  const key = e.name?.trim().toLowerCase()
  if (!key || seen.has(key)) continue
  seen.add(key)
  const muscle = e.primaryMuscles?.[0] || e.category || 'other'
  rows.push(`  (null, ${sql(e.name.trim())}, ${sql(muscle)}, ${sql(e.equipment)}, ${mechanic(e.mechanic)}, true)`)
}

const header = `-- Exercise library: add compound/isolation mechanic, reseed from free-exercise-db.
-- Source: ${SOURCE} (public domain / Unlicense). Generated by scripts/gen-exercise-seed.mjs.
-- Replaces the original 19 hand-seeded global exercises. Applied with the service role
-- (Supabase SQL editor / db push), which bypasses RLS on the exercise table.

alter table exercise add column if not exists mechanic text check (mechanic in ('compound','isolation'));

delete from exercise where owner_user_id is null;

insert into exercise (owner_user_id, name, muscle_group, equipment, mechanic, is_public) values
${rows.join(',\n')};
`

await writeFile(OUT, header)
console.log(`wrote ${OUT}\n${rows.length} exercises`)
